class Node:
    def __init__(self, val):
        self.val = val
        self.prev = None
        self.next = None


class LRUCache:
    # @param capacity, an integer
    def __init__(self, capacity):
        self.dict1 = {}
        self.max = capacity
        self.top = None
        self.capacity = 0
        self.end = None

    # @return an integer
    def get(self, key):
        if self.dict1.get(key, -1) == -1:
            return -1
        temp1 = self.top
        if self.end.val is key:
            self.end = self.end.prev
        if self.top.val is key:
            return self.dict1.get(key)
        while temp1:
            if temp1.val is key:
                self.top.prev = temp1
                temp = temp1.prev
                if temp:
                    temp.next = temp1.next
                    if temp1.next:
                        temp1.next.prev = temp
                temp1.next = self.top
                temp1.prev = None
                self.top = temp1
                return self.dict1.get(key)
            temp1 = temp1.next

    # @param key, an integer
    # @param value, an integer
    # @return nothing
    def set(self, key, value):
        if self.max == 0:
            return
        if self.dict1.get(key, -1) != -1:
            self.dict1[key] = value
            if self.top.val is key:
                return
            top1 = self.top
            while top1:
                if top1.val == key:
                    temp = top1.prev
                    if temp:
                        temp.next = top1.next
                        if top1.next:
                            top1.next.prev = temp
                    top1.next = self.top
                    self.top.prev = top1
                    self.top = top1
                    if self.end.next is not None:
                        self.end = self.end.prev
                    top1.prev = None
                    return
                top1 = top1.next
        if self.capacity == self.max:
            if self.top is self.end:
                self.dict1.pop(self.top.val, -1)
                self.top = None
            if self.end:
                self.dict1.pop(self.end.val, -1)
                self.end = self.end.prev
                if self.end:
                    self.end.next = None
            self.capacity -= 1
        self.capacity += 1
        self.dict1[key] = value
        temp = Node(key)
        temp.next = self.top
        if self.top:
            self.top.prev = temp
        self.top = temp
        if self.end is None:
            self.end = self.top

